; example2.asm  –  ReCOP ISA regression test  (v2: [AM:2][OP:6] encoding)
;
; 31 distinct ISA instruction forms + trailing NOOP at label "target" (PC=31).
; One instruction per ISA form, in the order they appear in the ISA table.
;
; Encodings shown use [AM:2][OP:6][Rz:4][Rx:4][OPERAND:16] bit order.
; ============================================================================

ORG 0

; ── AND ──────────────────────────────────────────────────────────────────────
AND  R1  R2  #0x00FF   ; 0x481200FF  AM=01 OP=001000 Rz=1 Rx=2 Op=0x00FF
AND  R1  R1  R2        ; 0xC8120000  AM=11 OP=001000 Rz=1 Rx=2

; ── OR ───────────────────────────────────────────────────────────────────────
OR   R3  R2  #0x0F     ; 0x4C32000F  AM=01 OP=001100 Rz=3 Rx=2 Op=0x000F
OR   R3  R3  R4        ; 0xCC340000  AM=11 OP=001100 Rz=3 Rx=4

; ── ADD ──────────────────────────────────────────────────────────────────────
ADD  R5  R6  #10       ; 0x7856000A  AM=01 OP=111000 Rz=5 Rx=6 Op=10
ADD  R5  R5  R6        ; 0xF8560000  AM=11 OP=111000 Rz=5 Rx=6

; ── SUBV / SUB ───────────────────────────────────────────────────────────────
SUBV R7  R8  #1        ; 0x43780001  AM=01 OP=000011 Rz=7 Rx=8 Op=1
SUB  R9  #5            ; 0x44900005  AM=01 OP=000100 Rz=9 Op=5  (Z only)

; ── LDR ──────────────────────────────────────────────────────────────────────
LDR  R10 #0x1234       ; 0x40A01234  AM=01 OP=000000 Rz=10 Op=0x1234
LDR  R11 R12           ; 0xC0BC0000  AM=11 OP=000000 Rz=11 Rx=12
LDR  R13 $20           ; 0x80D00014  AM=10 OP=000000 Rz=13 Op=20

; ── STR ──────────────────────────────────────────────────────────────────────
STR  R14 #42           ; 0x42E0002A  AM=01 OP=000010 Rz=14 Op=42  (M[R14]=42)
STR  R15 R1            ; 0xC2F10000  AM=11 OP=000010 Rz=15 Rx=1   (M[R15]=R1)
STR  R2  $30           ; 0x8202001E  AM=10 OP=000010 Rx=2  Op=30  (M[30]=R2)

; ── JMP ──────────────────────────────────────────────────────────────────────
JMP  target            ; 0x5800001F  AM=01 OP=011000 Op=31
JMP  R3                ; 0xD8030000  AM=11 OP=011000 Rx=3

; ── PRESENT ──────────────────────────────────────────────────────────────────
PRESENT R4  target     ; 0x5C40001F  AM=01 OP=011100 Rz=4 Op=31

; ── DATACALL ─────────────────────────────────────────────────────────────────
DATACALL R5            ; 0xE8050000  AM=11 OP=101000 Rx=5
DATACALL R6  #0x77     ; 0x69060077  AM=01 OP=101001 Rx=6 Op=0x77

; ── SZ ───────────────────────────────────────────────────────────────────────
SZ   target            ; 0x5400001F  AM=01 OP=010100 Op=31

; ── CLFZ ─────────────────────────────────────────────────────────────────────
CLFZ                   ; 0x10000000  AM=00 OP=010000

; ── LSIP / SSOP ──────────────────────────────────────────────────────────────
LSIP R7                ; 0xB7700000  AM=10 OP=110111 Rz=7
SSOP R8                ; 0xBA080000  AM=10 OP=111010 Rx=8

; ── NOOP ─────────────────────────────────────────────────────────────────────
NOOP                   ; 0x34000000  AM=00 OP=110100

; ── STRPC ────────────────────────────────────────────────────────────────────
STRPC $50              ; 0x9D000032  AM=10 OP=011101 Op=50

; ── CER / CEOT / SEOT ────────────────────────────────────────────────────────
CER                    ; 0x3C000000  AM=00 OP=111100
CEOT                   ; 0x3E000000  AM=00 OP=111110
SEOT                   ; 0x3F000000  AM=00 OP=111111

; ── LER ──────────────────────────────────────────────────────────────────────
LER  R9                ; 0xB6900000  AM=10 OP=110110 Rz=9

; ── SSVOP ────────────────────────────────────────────────────────────────────
SSVOP R10              ; 0xBB0A0000  AM=10 OP=111011 Rx=10

; ── MAX ──────────────────────────────────────────────────────────────────────
MAX  R11 #0xFFFF       ; 0x5EB0FFFF  AM=01 OP=011110 Rz=11 Op=0xFFFF

; ── target label (PC = 31 = 0x1F) ────────────────────────────────────────────
target:
NOOP                   ; 0x34000000  padding / branch target
